import pdfplumber
import pyperclip
import sys

if len(sys.argv) < 2:
    print("?????????????: python scrap.py <???? ? PDF>")
    sys.exit(1)

pdf_path = sys.argv[1]

all_text = ""

with pdfplumber.open(pdf_path) as pdf:
    for page in pdf.pages:
        text = page.extract_text()
        if text:
            all_text += text + "\n"

# ???????? ????? ? ????? ??????
pyperclip.copy(all_text)

